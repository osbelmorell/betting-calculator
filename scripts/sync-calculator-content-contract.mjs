import { readFileSync, writeFileSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const root = path.resolve(__dirname, '..');

const singleParlayPath = path.join(root, 'app/content/calculatorContent.ts');
const evPath = path.join(root, 'app/ev/content.ts');
const oddsPath = path.join(root, 'app/odds-converter/content.ts');
const contractPath = path.join(
  root,
  '.github/skills/calculator-content-alignment/CALCULATOR_CONTENT_CONTRACT.md',
);

function extractTypeBlock(source, typeName) {
  const marker = `type ${typeName} = {`;
  const start = source.indexOf(marker);

  if (start === -1) {
    throw new Error(`Could not find type block: ${typeName}`);
  }

  const bodyStart = source.indexOf('{', start);
  let depth = 0;
  let end = -1;

  for (let i = bodyStart; i < source.length; i += 1) {
    const char = source[i];
    if (char === '{') depth += 1;
    if (char === '}') {
      depth -= 1;
      if (depth === 0) {
        end = i;
        break;
      }
    }
  }

  if (end === -1) {
    throw new Error(`Unterminated type block: ${typeName}`);
  }

  return source.slice(bodyStart + 1, end);
}

function extractTopLevelKeys(typeBlock) {
  const lines = typeBlock.split('\n');
  const keys = [];
  let depth = 0;

  for (const line of lines) {
    const trimmed = line.trim();
    const openCount = (line.match(/{/g) || []).length;
    const closeCount = (line.match(/}/g) || []).length;

    if (depth === 0) {
      const match = trimmed.match(/^([a-zA-Z0-9_]+)\??:\s*/);
      if (match) {
        keys.push(match[1]);
      }
    }

    depth += openCount;
    depth -= closeCount;
  }

  return keys;
}

function intersection(arrays) {
  if (arrays.length === 0) return [];
  return arrays.reduce((acc, current) => acc.filter((key) => current.includes(key)));
}

function sortedUnique(values) {
  return [...new Set(values)].sort((a, b) => a.localeCompare(b));
}

function readKeys() {
  const singleParlaySource = readFileSync(singleParlayPath, 'utf8');
  const evSource = readFileSync(evPath, 'utf8');
  const oddsSource = readFileSync(oddsPath, 'utf8');

  const singleKeys = extractTopLevelKeys(extractTypeBlock(singleParlaySource, 'SingleCalculatorUiCopy'));
  const parlayKeys = extractTopLevelKeys(extractTypeBlock(singleParlaySource, 'ParlayCalculatorUiCopy'));
  const evKeys = extractTopLevelKeys(extractTypeBlock(evSource, 'EvCalculatorCopy'));
  const oddsKeys = extractTopLevelKeys(extractTypeBlock(oddsSource, 'OddsConverterCopy'));

  return {
    single: sortedUnique(singleKeys),
    parlay: sortedUnique(parlayKeys),
    ev: sortedUnique(evKeys),
    odds: sortedUnique(oddsKeys),
  };
}

function generateContractMarkdown(keysByCalculator) {
  const calculators = [
    ['Single', keysByCalculator.single],
    ['Parlay', keysByCalculator.parlay],
    ['EV', keysByCalculator.ev],
    ['Odds Converter', keysByCalculator.odds],
  ];

  const commonToAll = intersection(calculators.map(([, keys]) => keys));

  const lines = [];
  lines.push('# Calculator Content Contract');
  lines.push('');
  lines.push('This file is auto-generated by `npm run calc:contract:sync`.');
  lines.push('Do not edit manually.');
  lines.push('');
  lines.push('## Shared Keys Across All Calculators');
  lines.push('');

  if (commonToAll.length === 0) {
    lines.push('- No universal keys found.');
  } else {
    for (const key of commonToAll) {
      lines.push(`- ${key}`);
    }
  }

  lines.push('');
  lines.push('## Per-Calculator Key Matrix');
  lines.push('');

  for (const [name, keys] of calculators) {
    lines.push(`### ${name} (${keys.length} keys)`);
    lines.push('');
    for (const key of keys) {
      lines.push(`- ${key}`);
    }
    lines.push('');
  }

  lines.push('## Update Workflow');
  lines.push('');
  lines.push('1. Change calculator copy keys in source content files.');
  lines.push('2. Run `npm run calc:contract:sync`.');
  lines.push('3. Commit both source changes and this contract file together.');
  lines.push('');

  return `${lines.join('\n')}\n`;
}

function main() {
  const checkOnly = process.argv.includes('--check');
  const keysByCalculator = readKeys();
  const nextContent = generateContractMarkdown(keysByCalculator);

  if (checkOnly) {
    const currentContent = readFileSync(contractPath, 'utf8');
    if (currentContent !== nextContent) {
      console.error('Calculator content contract is outdated. Run: npm run calc:contract:sync');
      process.exit(1);
    }

    console.log('Calculator content contract is up to date.');
    return;
  }

  writeFileSync(contractPath, nextContent, 'utf8');
  console.log(`Updated ${path.relative(root, contractPath)}`);
}

main();
